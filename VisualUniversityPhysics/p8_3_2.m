gas=[2 4 20 28 32 38]*1.66E-27;   % H2 He Ne N2 O2 F2
k=1.38E-23;
t=300;
v=-1500:50:1500;

[V,Gas]=meshgrid(v,gas);
Fv=sqrt(Gas./(2*pi*k*t)).*exp(-Gas.*V.^2/(2*k*t));
figure
plot(v,Fv(1,:),'rx-',...
    v,Fv(2,:),'g*-',...
    v,Fv(3,:),'b+-',...
    v,Fv(4,:),'ko-',...
    v,Fv(5,:),'c^-',...
    v,Fv(6,:),'mv-','LineWidth',2)
title('不同气体分子麦克斯韦速度分布律')
legend(sprintf('H2'),...
    sprintf('He'),...
    sprintf('Ne'),...
    sprintf('N2'),...
    sprintf('O2'),...
    sprintf('F2'))

pause
close all;

