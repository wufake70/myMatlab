% 获取长度(维度值)
disp(length(b2));   % 返回长度10
disp(size(b2));     % 返回1,10 即1行，10列

% sum求和
disp(sum(b2));

% 索引取值***
disp(b2(2));    % 取单值
disp(b2(2:5));  % 指定索引范围取值
disp(b2(b2>=2&b2<=8));      % [2,8]的范围值
disp(b2(b2<=2|b2>=8));      % <=2|>=8的范围值
