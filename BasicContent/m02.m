disp('*******二维矩阵*******');
% 分号 分隔行元素,每一行元素个数必须相同
A = [14 22 3;34 51 36;440 40 50];

% 单引号矩阵转置(行列转换)
B1 = A';

% 生成全是零的矩阵
A2=zeros(3,2); % 3行，2列；可给第三个参数(层)；若只有一个参数，则生成方阵；
A3=ones(3,2);  % 全1矩阵；同上
A4=magic(5);    % 生成魔方(每一列元素只和相等)；只能传一个参数
disp(sum(A4));  % 对魔方求和，每一列元素相加得到向量，该项量元素值一样。

% 维度长度,size
t=size(A);
disp(sprintf('A的行长度为%d  A的列长度为%d',t(1),t(2)));

% 索引取值，下标从1开始
disp(A(2,2));   % 取单值
disp(A(1,:));   % 第一行元素
disp(A(1,A(1,:)>2));    % 第一行大于2的
% ...

% 极值
[Ma1,Index1]=max(A); % 得到每列中最大值(保存为在Ma1向量中)及其索引(...)
[ma,i]=max(Ma1);
index=Index1(i);
disp(sprintf('A的最大值%d,位置%d行 %d列',ma,index,i));


% (:)表示将矩阵按照列展开成为列向量。
