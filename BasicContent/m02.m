disp('*******一维矩阵*******');
% 一维矩阵(向量)
b=[0 1 2];  % 中括号赋值，可空格(逗号)分开

% 指定范围赋值
% 可指定步长
b1=0:10;     % 0到10，默认步长1，
% 可指定生成向量的长度
b2=linspace(1,10,10);  % 0到10，分成10分，
